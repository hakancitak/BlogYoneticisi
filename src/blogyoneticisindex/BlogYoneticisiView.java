    public void baslangicBilgisiniCek(){
        try {
            VeritabaniIslemleri vt=new VeritabaniIslemleri();
            vt.baglan();
            this.blogListesi=vt.tumBloglariGetir();
            jComboBoxBlogListesi.setModel(new DefaultComboBoxModel());
            jComboBoxBlogListesi.addItem("Seciniz...");
            if(this.blogListesi==null){
                return;}
            for (Blog b : this.blogListesi) {
                jComboBoxBlogListesi.addItem(Araclar.tarihZamnGoster(b.getOlusturmaTarihi())
                +"...."
                +b.getBlogBaslik());
                
            }
            vt.baglantiyiKes();
        } catch (Exception e) {
        Araclar.mesajGoster("Bir Hata Olustu.Açıklama: " + e.getMessage(),"HATA");
        }
    }
    public void kullaniciTablosuDoldur(){
        VeritabaniIslemleri vt=new VeritabaniIslemleri();
        try {
            vt.baglan();
            Object[][] veri=kullaniciBilgisiniIkiBoyutluDiziyeCevir(vt.tumKullanicilariGetir());
            Object [] baslik=vt.tabloKolonAdlariniGetir("kullanicilar");
            jTable1.setModel(Araclar.tabloIcinModelOlustur(baslik, veri));
            TableModel model=new DefaultTableModel(veri,baslik);
            jTable1.setModel(model);
            vt.baglantiyiKes();
        } catch (Exception e) {
            Araclar.mesajGoster("Bir Hata Olustu.Açıklama: " + e.getMessage(),"HATA");
        }
    
    }
       public Object[][] kullaniciBilgisiniIkiBoyutluDiziyeCevir(List<Kullanici> kList) {
        if (kList == null) {
            return null;
        }
        Object[][] sonuc = new Object[kList.size()][6];

        for (int i = 0; i < kList.size(); i++) {
            sonuc[i][0] = kList.get(i).getK_ID();
            sonuc[i][1] = kList.get(i).getK_Email();
            sonuc[i][2] = kList.get(i).getK_Sifre();
            sonuc[i][3] = kList.get(i).getK_adSoyad();
            sonuc[i][4] = kList.get(i).getK_Izin();
            sonuc[i][5] = kList.get(i).getKayitTarih();
        }


        return sonuc;
    }

    private void jButtonBloguTamamenSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBloguTamamenSilActionPerformed
         if (this.seciliBlog == null) {
            return;
        }
        int cevap = Araclar.soruSor("Bloğu silmek istediğinizden "
                + "emin misiniz(Blogtaki girdi ve "
                + "yorumlar otomatik olarak silinecek)?");
        if (cevap != 0) {
            return;
        }
        VeritabaniIslemleri vi = new VeritabaniIslemleri();
        try {
            vi.baglan();
            vi.bloguSil((Blog) seciliBlog);
            vi.baglantiyiKes();
            formuTemizle();
            baslangicBilgisiniCek();
        } catch (Exception ex) {
            Araclar.mesajGoster("Bir hata oluştu:Açıklama"
                    + ex.getMessage(), "HATA");
        }
    }//GEN-LAST:event_jButtonBloguTamamenSilActionPerformed


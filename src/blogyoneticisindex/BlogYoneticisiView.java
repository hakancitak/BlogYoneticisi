    private void jComboBoxKullaniciListesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxKullaniciListesiActionPerformed
         if (this.kullaniciListesi == null) {
            Araclar.mesajGoster("Kullanıcı Listesi Boş!!!", "UYARI");
            return;
        }
        String gecici = jComboBoxKullaniciListesi.getSelectedItem().toString();
        if (gecici.equals("Seçiniz...")) {
            Araclar.mesajGoster("Önce bir kullanıcı seçiniz!!!", "UYARI");
            return;
        }

        for(Kullanici k : this.kullaniciListesi) {
            if (gecici.contains(k.getK_Email())) {
                this.seciliKullanici = k;
            }
        }
    }//GEN-LAST:event_jComboBoxKullaniciListesiActionPerformed

    private void jButtonKullaniciBilgisiGörüntüleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKullaniciBilgisiGörüntüleActionPerformed
     if (this.seciliBlog == null) {
            Araclar.mesajGoster("Önce bir blog seçiniz!!!", "UYARI");
            return;
        }
        VeritabaniIslemleri vi = new VeritabaniIslemleri();
        try {
            vi.baglan();
            this.seciliKullanici = vi.kullaniciBilgisiGetir(this.seciliBlog.getK_ID());
            vi.baglantiyiKes();
        } catch (Exception ex) {
            Araclar.mesajGoster("Bir hata oluştu:Açıklama"
                    + ex.getMessage(), "HATA");
        }

        jDialogKullaniciBilgisiDetay.pack();
        jDialogKullaniciBilgisiDetay.setLocationRelativeTo(this);
        jDialogKullaniciBilgisiDetay.setVisible(true);

        jComboBoxKullaniciIzinlerDialogKullaniciDetay.setModel(new DefaultComboBoxModel());
        jComboBoxKullaniciIzinlerDialogKullaniciDetay.addItem("Yazar (Girdi ekleyebilir)");
        jComboBoxKullaniciIzinlerDialogKullaniciDetay.addItem("Okur (Yorum ekleyebilir)");
        jComboBoxKullaniciIzinlerDialogKullaniciDetay.addItem("Admin (Tüm verilere ulaşabilir)");

        jTextFieldAdSoyadDialogKullaniciDetay.setText(this.seciliKullanici.getK_adSoyad());
        jTextFieldKullaniciAdiDialogKullaniciDetay.setText(this.seciliKullanici.getK_Email());
        jPasswordFieldSifreDialogKullaniciDetay.setText(this.seciliKullanici.getK_Sifre());

        switch (this.seciliKullanici.getK_Izin().charAt(0)) {
            case 'Y':
                jComboBoxKullaniciIzinlerDialogKullaniciDetay.setSelectedIndex(0);
                break;
            case 'O':
                jComboBoxKullaniciIzinlerDialogKullaniciDetay.setSelectedIndex(1);
                break;
            case 'A':
                jComboBoxKullaniciIzinlerDialogKullaniciDetay.setSelectedIndex(2);
                break;
            default:
                break;
         }
    }//GEN-LAST:event_jButtonKullaniciBilgisiGörüntüleActionPerformed

